# to convert into another data type we can just write the name of the data type along with the data we need to pass

x = 5 # int data type 

# lets say I want to convert it to String then, 
y = str(x) # now value of c is of type string
print(y)

# to check the data type of a variable we can use type()

print(type(x)) # must print int
print(type(y)) # must print str

# similarly
y = float(x)
print(type(y))
print(y)





